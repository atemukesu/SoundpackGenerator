export function getDrumKeyName(note) {
  return GM_DRUM_KEY_NAMES[note] || "";
}

const NOTE_NAMES = ['C', 'C#', 'D', 'D#', 'E', 'F', 'F#', 'G', 'G#', 'A', 'A#', 'B'];

export function midiNoteName(note) {
  const octave = Math.floor(note / 12) - 1;
  const name = NOTE_NAMES[note % 12];
  return `${name}${octave}`;
}

export function getInstrumentName(id) {
  if (id === 128) return "标准鼓组";
  if (id >= 0 && id < GM_INSTRUMENTS.length) return GM_INSTRUMENTS[id];
  return `乐器 ${id}`;
}
